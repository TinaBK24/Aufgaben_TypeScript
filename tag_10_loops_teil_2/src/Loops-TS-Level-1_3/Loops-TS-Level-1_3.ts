console.log("%c for ", "background: gold; color: black;");
for (let num: number = 0; num <= 10; num++){
    console.log(`Hallo World ${num}`);
    
}

console.log("%c while ", "background: gold; color: black;");
let counter = 0;
while (counter <= 10){
    console.log(`Hallo World ${counter}`);
    
    counter++;
}