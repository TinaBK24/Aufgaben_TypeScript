//* Array-TS-Level-1_9 (push/pop shift/unshift)

let numberArr: number[] = [23, 54, 75];
console.log(numberArr);

numberArr.push(11, 32, 42, 5, 71);
console.log(numberArr);

numberArr.unshift(1, 3, 7, 8, 14);
console.log(numberArr);

numberArr.pop();
numberArr.pop();
console.log(numberArr);

numberArr.shift();
numberArr.shift();
console.log(numberArr);
